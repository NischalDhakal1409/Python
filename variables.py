#Types of Variables

#String data types
first_name = "Nischal"
last_name = "Dhakal"
full_name = first_name + " " + last_name
print (full_name)
print(type(first_name))

#Integrer Data type
age = 21
age += 1
print(age)
print(type(age))
print("Your age is: " + str(age))

#Float Data Types
height = 55.6
print(height)
print(type(height))
print("Your height is : " + str(height))

#Boolean Data Type
panda = True
print(panda)
print(type(panda))
print("Is it an animal? : " + str(panda))
